package net.konic.corejava.HashMap_LinkedHashMap;

public class StudentData {

	int rollNum;
	String name;
	int marks;

	StudentData() {

		this.rollNum = rollNum;
		this.name = name;
		this.marks = marks;

	}

	@Override
	public String toString() {
		return "StudentData [rollNum=" + rollNum + ", name=" + name + ", marks=" + marks + "]";
	}

}
